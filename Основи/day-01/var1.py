# Створи програму для обчислення площі прямокутника (ввід довжини/ширини через input, вивід через print). Напиши програму, яка конвертує температуру з Цельсія у Фаренгейт.
length = float(input("Введіть довжину прямокутника: "))
width = float(input("Введіть ширину прямокутника: "))
area = length * width
print(f"Площа прямокутника: {area}")

celsius = float(input("Введіть температуру в Цельсії: "))
fahrenheit = (celsius * 9/5) + 32
print(f"Температура в Фаренгейті: {fahrenheit}")